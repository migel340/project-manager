/**
 * Enumeration representing the possible priorities of a task
 * @author spacedesk2
 * @version 1.0
 */

public enum TaskPriority {
    /** Priority has not been set */
    NOT_SET,
    /** Low priority */
    LOW,
    /** Medium priority */
    MEDIUM,
    /** High priority */
    HIGH
}